export default function ContactButtonModal() {
  const [open, setOpen] = useState(false);

  useEffect(() => {
    function onKeyDown(e) { if (e.key === "Escape") setOpen(false); }
    if (open) window.addEventListener("keydown", onKeyDown);
    return () => window.removeEventListener("keydown", onKeyDown);
  }, [open]);

  useEffect(() => {
    if (!open) return;
    const prev = document.body.style.overflow;
    document.body.style.overflow = "hidden";
    return () => { document.body.style.overflow = prev; };
  }, [open]);

  return (
    <>
      <button className="btn" onClick={() => setOpen(true)}>
        Contáctenos
      </button>

      {open && (
        <div
          className="modalOverlay"
          onClick={() => setOpen(false)}
        >
          <div className="modalCard" onClick={(e) => e.stopPropagation()}>
            <h3 className="modalTitle pd">Contacto</h3>

            <div className="modalList">
              {contacts.map(({ icon, label, detail, href, color, bg, border, target }) => (
                <a
                  key={label}
                  className="modalItem"
                  href={href}
                  target={target}
                  rel="noreferrer"
                  style={{ background: bg, borderColor: border }}
                >
                  <span className="modalItemIcon" style={{ color }}>{icon}</span>
                  <span className="modalItemText">
                    <span className="modalItemLabel" style={{ color }}>{label}</span>
                    <span className="modalItemDetail">{detail}</span>
                  </span>
                </a>
              ))}
            </div>

            <button className="btn modalClose" onClick={() => setOpen(false)}>
              Cerrar
            </button>
          </div>
        </div>
      )}
    </>
  );
}